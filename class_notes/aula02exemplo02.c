#include <stdio.h>

int main() {
    int x, y;
    char a, b;
    x = 65;
    y = 97;
    a = 'a';
    b = 'A';
    printf("O número %d na tabela ASCII corresponde ao caractere %c\n", x, x);
    printf("O número %d na tabela ASCII corresponde ao caractere %c\n", y, y);
    printf("O caractere %c corresponde ao número %d na tabela ASCII\n", a, a);
    printf("O caractere %c corresponde ao número %d na tabela ASCII\n", b, b);
}